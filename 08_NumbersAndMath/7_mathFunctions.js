let x = -10;

console.log(Math.abs(x)); // 10

// Math.abs() convert negative value to positive value

let y = 50.5;

console.log(Math.round(y)); // 51

// Math.round() convert float value to the int value

let z = 4.7;

console.log(Math.ceil(z)); // 5

console.log(Math.floor(z)); // 4

// Math.ceil() always return top value and Math.floor() always return bottom value

console.log(Math.sqrt(25)); // 5

console.log(Math.max(10, 12, 5, 15, 11)); // return the max value from the elements -- 15

console.log(Math.min(10, 12, 5, 15, 11)); // return thed min value from the elements -- 5
